            }

            $null = $PSBoundParameters.Remove('ClientIPAddress')
        }
        else
        {
            if(!$PSBoundParameters.ContainsKey('Name'))
            {
                $PSBoundParameters['Name'] = "undefined"
            }
        }

        Az.PostgreSql.internal\New-AzPostgreSqlFirewallRule @PSBoundParameters
    } catch {
        throw
    }
}
